#!/usr/bin/env bash
#
# claude-code-pipeline/init.sh
# Bootstrap script: links the pipeline into a target project's .claude/ directory
#
# Usage:
#   bash init.sh /path/to/project [--stack=swift-ios|react|python] [--force]
#

set -euo pipefail

# --- Constants ---
PIPELINE_ROOT="$(cd "$(dirname "$0")" && pwd)"
SCRIPT_NAME="$(basename "$0")"

# --- Helpers ---
info()  { echo "[pipeline] $*"; }
warn()  { echo "[pipeline] WARNING: $*" >&2; }
error() { echo "[pipeline] ERROR: $*" >&2; exit 1; }

usage() {
    cat <<EOF
Usage: bash $SCRIPT_NAME <project-dir> [OPTIONS]

Bootstrap the Claude Code Pipeline into a project.

Arguments:
  project-dir          Path to the target project root

Options:
  --stack=<name>       Force a specific adapter (swift-ios, react, python)
                       Auto-detected from project files if omitted
  --force              Overwrite existing symlinks and config
  --help               Show this help message

Examples:
  bash $SCRIPT_NAME .
  bash $SCRIPT_NAME /path/to/myapp --stack=react
  bash $SCRIPT_NAME . --force
EOF
    exit 0
}

# --- Parse arguments ---
PROJECT_DIR=""
STACK=""
FORCE=false

for arg in "$@"; do
    case "$arg" in
        --stack=*)  STACK="${arg#--stack=}" ;;
        --force)    FORCE=true ;;
        --help|-h)  usage ;;
        -*)         error "Unknown option: $arg" ;;
        *)
            if [ -z "$PROJECT_DIR" ]; then
                PROJECT_DIR="$arg"
            else
                error "Unexpected argument: $arg"
            fi
            ;;
    esac
done

[ -z "$PROJECT_DIR" ] && error "Missing required argument: project-dir. Run with --help for usage."
[ -d "$PROJECT_DIR" ] || error "Project directory does not exist: $PROJECT_DIR"

PROJECT_DIR="$(cd "$PROJECT_DIR" && pwd)"

# --- Step 1: Ensure .claude/ exists ---
CLAUDE_DIR="$PROJECT_DIR/.claude"
mkdir -p "$CLAUDE_DIR"
mkdir -p "$CLAUDE_DIR/tmp"

# --- Step 2: Stack detection ---
detect_stack() {
    local dir="$1"

    # Check for Swift/iOS
    for f in "$dir"/*.xcodeproj "$dir"/*.xcworkspace; do
        if [ -e "$f" ]; then
            echo "swift-ios"
            return
        fi
    done
    if [ -f "$dir/Package.swift" ]; then
        echo "swift-ios"
        return
    fi

    # Check for React (package.json with react dependency)
    if [ -f "$dir/package.json" ]; then
        if grep -q '"react"' "$dir/package.json" 2>/dev/null; then
            echo "react"
            return
        fi
        # If package.json exists but no react, could be a Node project
        # Fall through to check for Python
    fi

    # Check for Python
    if [ -f "$dir/pyproject.toml" ] || [ -f "$dir/setup.py" ] || [ -f "$dir/setup.cfg" ] || [ -f "$dir/requirements.txt" ]; then
        echo "python"
        return
    fi

    # Check for generic Node/TypeScript (no react)
    if [ -f "$dir/package.json" ]; then
        echo "react"  # Use react adapter as closest match for JS/TS projects
        return
    fi

    echo ""
}

if [ -z "$STACK" ]; then
    STACK=$(detect_stack "$PROJECT_DIR")
    if [ -z "$STACK" ]; then
        error "Could not auto-detect tech stack. Use --stack=<name> to specify.\nAvailable: swift-ios, react, python"
    fi
    info "Auto-detected stack: $STACK"
else
    info "Using specified stack: $STACK"
fi

ADAPTER_DIR="$PIPELINE_ROOT/adapters/$STACK"
[ -d "$ADAPTER_DIR" ] || error "Adapter not found: $ADAPTER_DIR"

# --- Step 3: Write pipeline.config ---
CONFIG_FILE="$CLAUDE_DIR/pipeline.config"
if [ -f "$CONFIG_FILE" ] && [ "$FORCE" = false ]; then
    info "pipeline.config already exists. Use --force to overwrite."
else
    cat > "$CONFIG_FILE" <<EOF
# Claude Code Pipeline Configuration
# Generated by init.sh — edit as needed

# Active tech-stack adapter
stack=$STACK

# Absolute path to the pipeline repo
pipeline_root=$PIPELINE_ROOT

# Date this config was generated
initialized=$(date -u +"%Y-%m-%dT%H:%M:%SZ")
EOF
    info "Wrote $CONFIG_FILE"
fi

# --- Step 4: Create symlinks ---
create_symlink() {
    local target="$1"
    local link="$2"
    local desc="$3"

    if [ -L "$link" ]; then
        if [ "$FORCE" = true ]; then
            rm "$link"
        else
            local existing
            existing=$(readlink "$link" 2>/dev/null || echo "unknown")
            if [ "$existing" = "$target" ]; then
                info "Symlink already correct: $desc"
                return
            fi
            warn "Symlink exists but points elsewhere: $link -> $existing (expected $target). Use --force to fix."
            return
        fi
    elif [ -e "$link" ]; then
        if [ "$FORCE" = true ]; then
            # It's a real directory/file — back it up
            local backup="${link}.backup.$(date +%s)"
            mv "$link" "$backup"
            warn "Backed up existing $desc to $backup"
        else
            warn "$desc already exists as a regular file/directory. Use --force to replace (will backup)."
            return
        fi
    fi

    ln -s "$target" "$link"
    info "Linked: $desc -> $target"
}

# Agents
create_symlink "$PIPELINE_ROOT/agents" "$CLAUDE_DIR/agents" "agents"

# Skills
create_symlink "$PIPELINE_ROOT/skills" "$CLAUDE_DIR/skills" "skills"

# Scripts (adapter-specific)
create_symlink "$ADAPTER_DIR/scripts" "$CLAUDE_DIR/scripts" "scripts"

# --- Step 5: Merge hooks into settings.json ---
ADAPTER_HOOKS="$ADAPTER_DIR/hooks.json"
SETTINGS_FILE="$CLAUDE_DIR/settings.json"

if [ -f "$ADAPTER_HOOKS" ]; then
    if command -v jq &>/dev/null; then
        if [ -f "$SETTINGS_FILE" ]; then
            # Merge: adapter hooks appended to existing hooks
            # Use jq to combine the PreToolUse arrays
            MERGED=$(jq -s '
                (.[0].hooks.PreToolUse // []) as $existing |
                (.[1].hooks.PreToolUse // []) as $adapter |
                .[0] * {hooks: {PreToolUse: ($existing + $adapter | unique_by(.matcher + (.hooks | tostring)))}}
            ' "$SETTINGS_FILE" "$ADAPTER_HOOKS" 2>/dev/null || echo "")

            if [ -n "$MERGED" ]; then
                echo "$MERGED" > "$SETTINGS_FILE"
                info "Merged adapter hooks into existing settings.json"
            else
                warn "Could not merge hooks. Copying adapter hooks as settings.json."
                cp "$ADAPTER_HOOKS" "$SETTINGS_FILE"
            fi
        else
            cp "$ADAPTER_HOOKS" "$SETTINGS_FILE"
            info "Created settings.json from adapter hooks"
        fi
    else
        if [ -f "$SETTINGS_FILE" ]; then
            warn "jq not installed — cannot merge hooks into existing settings.json. Install jq or merge manually."
        else
            cp "$ADAPTER_HOOKS" "$SETTINGS_FILE"
            info "Created settings.json from adapter hooks (install jq for merge support)"
        fi
    fi
fi

# --- Step 6: Generate CLAUDE.md if not present ---
CLAUDE_MD="$CLAUDE_DIR/CLAUDE.md"
if [ ! -f "$CLAUDE_MD" ]; then
    TEMPLATE="$PIPELINE_ROOT/templates/CLAUDE.md.template"
    if [ -f "$TEMPLATE" ]; then
        cp "$TEMPLATE" "$CLAUDE_MD"
        info "Generated CLAUDE.md from template (edit to customize)"
    fi
else
    info "CLAUDE.md already exists — not overwriting"
fi

# --- Step 7: Generate ORCHESTRATOR.md skeleton if not present ---
ORCHESTRATOR_MD="$CLAUDE_DIR/ORCHESTRATOR.md"
if [ ! -f "$ORCHESTRATOR_MD" ]; then
    TEMPLATE="$PIPELINE_ROOT/templates/ORCHESTRATOR.md.template"
    if [ -f "$TEMPLATE" ]; then
        cp "$TEMPLATE" "$ORCHESTRATOR_MD"
        info "Generated ORCHESTRATOR.md skeleton from template (edit to customize)"
    fi
else
    info "ORCHESTRATOR.md already exists — not overwriting"
fi

# --- Step 8: Summary ---
echo ""
echo "========================================"
echo "  Claude Code Pipeline initialized!"
echo "========================================"
echo ""
echo "  Project:    $PROJECT_DIR"
echo "  Stack:      $STACK"
echo "  Pipeline:   $PIPELINE_ROOT"
echo ""
echo "  Symlinks:"
echo "    .claude/agents  -> pipeline/agents"
echo "    .claude/skills  -> pipeline/skills"
echo "    .claude/scripts -> pipeline/adapters/$STACK/scripts"
echo ""
echo "  Config:     .claude/pipeline.config"
echo "  Hooks:      .claude/settings.json"
echo ""
echo "  Next steps:"
echo "    1. Edit .claude/CLAUDE.md with your project description"
echo "    2. Edit .claude/ORCHESTRATOR.md with your architecture"
echo "    3. Run 'claude' and use /orchestrate to start the pipeline"
echo ""
